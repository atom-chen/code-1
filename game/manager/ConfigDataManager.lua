function ConfigDataManager:getRewardConfigById(id)
    local rewardConfig = self:getConfigById(ConfigData.FixRewardConfig,id)
    if rewardConfig == nil then
        return
    end

    local data = self:getRewardConfigByJson(rewardConfig.reward)
    return data
